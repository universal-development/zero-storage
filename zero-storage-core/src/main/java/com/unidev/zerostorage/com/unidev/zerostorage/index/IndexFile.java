package com.unidev.zerostorage.com.unidev.zerostorage.index;

/**
 * Index file name
 */
public class IndexFile {

    private String file;

    public IndexFile() {

    }

    public IndexFile(String file) {
        this.file = file;
    }

    public String file() {
        return this.file;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }
}
